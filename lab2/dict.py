# Создаем словарь
fruit_dict = {
    "яблоко": "красное",
    "банан": "желтый",
    "вишня": "красная"
}

# Доступ к элементам словаря
print("Цвет яблока:", fruit_dict["яблоко"])

# Изменение элементов
fruit_dict["банан"] = "зеленый"
print("\nПосле изменения цвета банана:", fruit_dict)

# Добавление новых элементов
fruit_dict["апельсин"] = "оранжевый"
print("\nПосле добавления апельсина:", fruit_dict)

# Удаление элементов
fruit_dict.pop("вишня")
print("\nПосле удаления вишни:", fruit_dict)

# Перебор словаря
print("\nВсе элементы в словаре:")
for key, value in fruit_dict.items():
    print(f"{key}: {value}")

# Копирование словаря
fruit_dict_copy = fruit_dict.copy()
print("\nКопия словаря:", fruit_dict_copy)

# Вложенные словари
nested_dict = {
    "фрукты": {
        "яблоко": "красное",
        "банан": "желтый"
    },
    "овощи": {
        "морковь": "оранжевая",
        "огурец": "зеленый"
    }
}
print("\nВложенный словарь:", nested_dict)

# Методы словарей
print("\nКлючи словаря:", fruit_dict.keys())
print("Значения словаря:", fruit_dict.values())
print("Элементы словаря:", fruit_dict.items())

# Упражнение: пользователь вводит элементы словаря
user_dict = {}
print("\nВведите 3 пары ключ-значение для словаря:")
for _ in range(3):
    key = input("Введите ключ: ")
    value = input("Введите значение: ")
    user_dict[key] = value

print("\nВаш словарь:", user_dict)
