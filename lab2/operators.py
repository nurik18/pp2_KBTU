# Арифметические операторы
aValue = 10
bValue = 3
print("Сложение:", aValue + bValue)
print("Вычитание:", aValue - bValue)
print("Умножение:", aValue * bValue)
print("Деление:", aValue / bValue)
print("Целочисленное деление:", aValue // bValue)
print("Остаток от деления:", aValue % bValue)
print("Возведение в степень:", aValue ** bValue)

# Операторы сравнения
xValue = 5
yValue = 10
print("x равно y:", xValue == yValue)
print("x не равно y:", xValue != yValue)
print("x больше y:", xValue > yValue)
print("x меньше или равно y:", xValue <= yValue)

# Логические операторы
isSunny = True
isWarm = False
print("Сегодня хорошая погода:", isSunny and isWarm)
print("Можно выйти на улицу:", isSunny or isWarm)
print("Не холодно:", not isWarm)